public class ArithmeticOperator {
    public static void main(String[] args) {
        int a = 32, b = 23;

        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a % b = " + (a % b));
    }
}