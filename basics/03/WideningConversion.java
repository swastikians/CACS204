public class WideningConversion {
    public static void main(String[] args) {
        int n = 100;
        float f = 69f;
        double d = n;
        char c = 'A';
        int ascii = c;

        System.out.println("int n = " + n);
        System.out.println("float f = " + f);
        System.out.println("double d = " + d);
        System.out.println("char c = " + c + " | int ascii = " + ascii);
    }
}
